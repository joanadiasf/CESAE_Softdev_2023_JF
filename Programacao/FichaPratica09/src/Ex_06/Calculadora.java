package Ex_06;

public class Calculadora {

    /**
     * construtor
     */
    public Calculadora() {
    }

    /**
     * Método para fazer a adição de dois números
     * @param num1 - numero 1
     * @param num2 - numero 2
     * @return - resultado
     */
    public static double adicao(double num1, double num2){

        return num1 + num2;

    }

    /**
     * Método para fazer a subtração de dois números
     * @param num1 - número 1
     * @param num2 - número 2
     * @return - resultado
     */

    public static double subtracao(double num1, double num2){

        return num1 - num2;

    }

    /**
     * Método para fazer a multiplicação de dois números
     * @param num1 - número 1
     * @param num2 - número 2
     * @return - resultado
     */
    public static double multiplicacao(double num1, double num2){

        return num1*num2;

    }

    /**
     * Método para fazer a divisão de dois números
     * @param num1 - número 1
     * @param num2 - número 2
     * @return - resultado
     */
    public static double divisao(double num1, double num2){

        return num1/num2;

    }

}

